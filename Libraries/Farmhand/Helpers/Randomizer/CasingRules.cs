namespace Farmhand.Helpers.Randomizer
{
    /// <summary>
    /// An enumeration of casing rules for the randomizer.
    /// </summary>
    public enum CasingRules
    {
        /// <summary>
        /// Lower case, e.g., "example string"
        /// </summary>
        LowerCase,

        /// <summary>
        /// Upper case, e.g., "EXAMPLE STRING"
        /// </summary>
        UpperCase,

        /// <summary>
        /// Mixed case, e.g., "ExaMPle StrING"
        /// </summary>
        MixedCase
    }
}
